class Dog

    def initialize(name,breed,age,bark,favorite_foods)
        @name = name
        @breed = breed
        @age = age
        @favorite_foods = favorite_foods
    end

    def name
        name
    end

    def breed
        breed
    end

    def age
        age
    end

    def bark
        bark
    end
    
    def favorite_foods
        favorite_foods
    end

end
